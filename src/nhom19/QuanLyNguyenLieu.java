    public ArrayList<NguyenLieu> getData() {
        ArrayList<NguyenLieu> dsNl = new ArrayList<NguyenLieu>();
        try{
            String query = "select * from NguyenLieu";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            NguyenLieu n;
            while(rs.next()) {
                n = new NguyenLieu(rs.getString("maNl"), rs.getString("tenNl"), rs.getString("xuatXu"), rs.getString("donGia"));
                dsNl.add(n);
            }
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return dsNl;
    }
