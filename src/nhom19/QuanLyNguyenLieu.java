    private NguyenLieu getNguyenLieuDetail() {
        String maNl = jTextField2.getText().trim();
        String tenNl = jTextField3.getText().trim();
        String xuatXu = jTextField4.getText().trim();
        String donGia = jTextField5.getText().trim();
        
        return new NguyenLieu(maNl, tenNl, xuatXu, donGia);
    }
     void insertNguyenLieuIntoDB(String maNl, String tenNl, String xuatXu, String donGia) {
        try{
            String query = "insert into NguyenLieu values(?,?,?,?)";
            PreparedStatement pst = con.preparedStatement(query);
            
            pst.setString(1, maNl);
            pst.setString(2, tenNl);
            pst.setString(3, xuatXu);
            pst.setString(4, donGia);
            
            pst.executeUpdate();
            
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
     void updateNguyenLieu(String maNl, String tenNl, String xuatXu, String donGia) {
         try{
            String query = "update NguyenLieu set tenNl=?, xuatXu=?, donGia=? where maNl=?";
            PreparedStatement pst = con.preparedStatement(query);
            
            pst.setString(4, maNl);
            pst.setString(1, tenNl);
            pst.setString(2, xuatXu);
            pst.setString(3, donGia);
            
            pst.executeUpdate();
            
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
     }
     void deleteNguyenLieu(String maNl) {
         try{
            String query = "delete from NguyenLieu where maNl=?";
            PreparedStatement pst = con.preparedStatement(query);
            
            
            pst.setString(1, maNl);
            
            
            pst.executeUpdate();
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
     }
