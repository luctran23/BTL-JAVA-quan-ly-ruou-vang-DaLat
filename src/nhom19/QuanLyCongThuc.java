    private CongThuc getCongThucDetail() {
        String maCt = jTextField2.getText().trim();
        String tenCt = jTextField3.getText().trim();
        String qua = jTextField4.getText().trim();
        String men = jTextField5.getText().trim();
        String nuoc = jTextField6.getText().trim();
        
        return new CongThuc(maCt, tenCt, qua, men, nuoc);
    }
    void insertCongThucIntoDB(String maCt, String tenCt, String qua, String men, String nuoc) {
        try{
            String query = "insert into CongThuc values(?,?,?,?,?)";
            PreparedStatement pst = con.preparedStatement(query);
            
            pst.setString(1, maCt);
            pst.setString(2, tenCt);
            pst.setString(3, qua);
            pst.setString(4, men);
            pst.setString(5, nuoc);
            
            pst.executeUpdate();
            
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Khong them duoc");
        }
    }
    void updateCongThuc(String maCt, String tenCt, String qua, String men, String nuoc) {
        try{
            String query = "update CongThuc set tenCt=?, qua=?, men=?, nuoc=? where maCt=?";
            PreparedStatement pst = con.preparedStatement(query);
            
            pst.setString(5, maCt);
            pst.setString(1, tenCt);
            pst.setString(2, qua);
            pst.setString(3, men);
            pst.setString(4, nuoc);
            
            pst.executeUpdate();
            
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Khong sua duoc");
        }
    }
    void deleteCongThuc(String maCt){
        try{
            String query = "delete from CongThuc where maCt=?";
            PreparedStatement pst = con.preparedStatement(query);
            
            
            pst.setString(1, maCt);
            
            
            pst.executeUpdate();
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
