    public ArrayList<CongThuc> getData() {
        ArrayList<CongThuc> dsCt = new ArrayList<CongThuc>();
        try {
            String query = "select * from CongThuc";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            CongThuc c;
            while(rs.next()) {
                c = new CongThuc(rs.getString("maCt"), rs.getString("tenCt"), rs.getString("qua"), rs.getString("men"), rs.getString("nuoc"));
                dsCt.add(c);
            }
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Khong the doc du lieu");
        }
        return dsCt;
    }
