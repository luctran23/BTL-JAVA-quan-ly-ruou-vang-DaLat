    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int ptQua = Integer.parseInt(jTextField4.getText());
        int ptMen = Integer.parseInt(jTextField5.getText());
        int ptNuoc = Integer.parseInt(jTextField6.getText());
        if( ptQua <= 100 && ptMen <= (100 - ptQua) && ptNuoc <= (100-ptQua-ptMen) && (ptQua + ptMen + ptNuoc) == 100) {
            CongThuc c = getCongThucDetail();
            insertCongThucIntoDB(c.getMaCt(), c.getTenCt(), c.getPhanTramQua(), c.getPhanTramMen(), c.getPhanTramNuoc());
            loadTable(getData());
        }
        else {
            JOptionPane.showMessageDialog(null, "Tỷ lệ không hợp lý");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

